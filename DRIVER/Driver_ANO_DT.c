//移植时，用户应根据自身应用的情况，根据使用的通信方式，实现此函数
void ANO_DT_Send_Data(u8 *dataToSend , u8 length)
{
	u8 i=0;
	while(i<length)
	{
	USART1->DR = (dataToSend[i]&(uint16_t)0x01FF);
	while(USART_GetFlagStatus(USART1,USART_FLAG_TC) == RESET);
	i++;
	}
}
